def parse_trips_per_day(txt):
    """'1일 N회' → N (integer). '1일 N회' 패턴에서 N을 추출. 실패 시 None."""
    if not txt:
        return None
    import re
    # '1일 N회' 패턴 — '회' 앞의 숫자를 추출
    m = re.search(r"(\d+)\s*회", txt)
    if m:
        return int(m.group(1))
    # fallback: 첫 번째 숫자
    m2 = re.search(r"(\d+)", txt)
    return int(m2.group(1)) if m2 else None


